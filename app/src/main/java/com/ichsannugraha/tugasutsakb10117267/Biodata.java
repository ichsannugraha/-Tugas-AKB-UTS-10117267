package com.ichsannugraha.tugasutsakb10117267;

public class Biodata {
    private String nim;
    private String nama;
    private String kelas;
    private String telepon;
    private String email;
    private String sosmed;

    public Biodata(String nim, String nama, String kelas, String telepon, String email, String sosmed) {
        this.nim = nim;
        this.nama = nama;
        this.kelas = kelas;
        this.telepon = telepon;
        this.email = email;
        this.sosmed = sosmed;
    }

    public String getNim() {
        return nim;
    }

    public String getNama() {
        return nama;
    }

    public String getKelas() {
        return kelas;
    }

    public String getTelepon() {
        return telepon;
    }

    public String getEmail() {
        return email;
    }

    public String getSosmed() {
        return sosmed;
    }
}


